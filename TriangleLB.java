package java;
import java.util.Scanner;

public class TriangleLB {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.print("높이: ");
		int num = sc.nextInt();
		
		for(int i =1; i <= num ; i++) {
			for(int j = 1; j <= 1; j++) {
				System.out.print("*");
			
			}
			System.out.println("\n");
		}

	}
}
